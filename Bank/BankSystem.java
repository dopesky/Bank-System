    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        boolean viable=false;
        accountNumber=txtAccount.getText().trim();
        pin=txtPin.getText();
        for(int i=0;i<accounts.size();i++){
            if(accounts.get(i).get(0).equalsIgnoreCase(accountNumber)&&accounts.get(i).get(4).equalsIgnoreCase(pin)){
                viable=true;
                setVisible(false);
                setData();
                frameStatus.setResizable(false);
                frameStatus.pack();
                frameStatus.setLocationRelativeTo(null);
                frameStatus.setVisible(true);
            }
        }
        if(!viable){
            JOptionPane.showMessageDialog(null,"Login Unsuccessful\nInvalid Credentials","Error",JOptionPane.ERROR_MESSAGE);
            txtAccount.setText("");
            txtPin.setText("");
            txtAccount.requestFocus();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

