    private void getData(){
        try{
            Scanner in=new Scanner(new FileInputStream("AccountData.txt")).useDelimiter("\n");
            while(in.hasNext()){
                ArrayList<String> temp=new ArrayList<>();
                String next=in.next().trim();
                Scanner inner=new Scanner(next).useDelimiter("//");
                while(inner.hasNext()){
                    String Inner=inner.next().trim();
                    temp.add(Inner);
                }
                accounts.add(temp);
            }
        }catch(FileNotFoundException fnfe){}
    }
    
    private void setData(){
        for(int i=0;i<accounts.size();i++){
            if(accounts.get(i).get(0).equalsIgnoreCase(accountNumber)){
                txtAcc.setText(accounts.get(i).get(0));
                txtNm.setText(accounts.get(i).get(1));
                txtType.setText(accounts.get(i).get(2));
                txtBalance.setText(accounts.get(i).get(3));
                comboStatus.setSelectedItem(txtType.getText());
            }
        }
    }
    
