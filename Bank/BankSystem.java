    private void btnDepositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositActionPerformed
        if(evt.getSource().equals(btnDeposit)){
            String amount=txtTransaction.getText().trim();
            try{
                for(int i=0;i<accounts.size();i++){
                    if(accounts.get(i).get(0).equalsIgnoreCase(accountNumber)){
                        int before=Integer.parseInt(accounts.get(i).get(3));
                        int add=Integer.parseInt(amount);
                        int balance=before+add;
                        accounts.get(i).set(3,String.valueOf(balance));
                        setData();
                        txtTransaction.setText("");
                    }
                }
            }catch(NumberFormatException nfe){
                JOptionPane.showMessageDialog(null,"Use Numbers for Transactions","Error",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            String amount=txtTransaction.getText().trim();
            try{
                for(int i=0;i<accounts.size();i++){
                    if(accounts.get(i).get(0).equalsIgnoreCase(accountNumber)){
                        int before=Integer.parseInt(accounts.get(i).get(3));
                        int deduct=Integer.parseInt(amount);
                        int balance=before-deduct;
                        if(balance<0){
                            JOptionPane.showMessageDialog(null,"You cannot withdraw more than you have","Error",JOptionPane.ERROR_MESSAGE);
                            txtTransaction.setText("");
                            return;
                        }
                        accounts.get(i).set(3,String.valueOf(balance));
                        setData();
                        txtTransaction.setText("");
                    }
                }
            }catch(NumberFormatException nfe){
                JOptionPane.showMessageDialog(null,"Use Numbers for Transactions","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDepositActionPerformed

